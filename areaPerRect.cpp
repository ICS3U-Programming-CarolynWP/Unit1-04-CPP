// Displays rectangle with length of 20m and width of 34m

#include <iostream>

int main() {
    std::cout << "Rectangle Area and Perimeter:\n";
    std::cout << "length is 20m and width is 34m:\n";
    std::cout << "The area is = " << 20 * 34 << " m^2\n";
    std::cout << "The perimeter is = " << 2 * (20 + 34) << " m\n";
}
